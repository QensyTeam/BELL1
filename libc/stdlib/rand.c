#include <stdint.h>
#include <stdio.h>

static uint32_t seed = 1; // Начальное значение для генератора случайных чисел

int rand(void) {
    // Линейный конгруэнтный генератор случайных чисел (LCG)
    // Параметры для простого LCG: 
    //   X(n+1) = (a * X(n) + c) % m
    // где a = 1103515245, c = 12345, m = 2^31
    // Вы можете использовать другие параметры в зависимости от ваших требований

    const uint32_t a = 1103515245;
    const uint32_t c = 12345;
    const uint32_t m = 1U << 31; // 2^31

    seed = (a * seed + c) % m;
    return (int)(seed / 65536) % 32768; // Приведение к диапазону [0, 32767]
}