#include <stdio.h>

int isspace(int c) {
    return (c == ' '  ||  // пробел
            c == '\t' ||  // табуляция
            c == '\n' ||  // перевод строки
            c == '\v' ||  // вертикальная табуляция
            c == '\f' ||  // прогон страницы
            c == '\r');   // возврат каретки
}
